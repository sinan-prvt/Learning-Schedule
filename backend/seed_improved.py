def seed():
    Week.objects.all().delete()
    Day.objects.all().delete()
    
    print("Starting Comprehensive Roadmap Seeding...")

    for w_idx, w_info in enumerate(WEEKS_DATA):
        week = Week.objects.create(
            week_id=w_info["id"], 
            title=w_info["title"], 
            is_active=(w_info["id"] == "W1")
        )
        
        for d_idx, topic in enumerate(w_info["days"]):
            day = Day.objects.create(
                week=week,
                day_number=d_idx + 1,
                topic=topic,
                category=w_info["title"],
                duration="8h"
            )
            
            # Setup Sessions
            for s_idx in range(5):
                Session.objects.create(
                    day=day,
                    time=f"{9 + s_idx}:00 AM",
                    title=f"Session {s_idx + 1} on {topic}",
                    description=f"Deep dive exploration into {topic} - Module {s_idx + 1}."
                )

            # Assign Topic-Specific Questions
            content = DAY_CONTENT.get(topic, {})
            quiz_questions = content.get("quiz", [])
            
            # If no topic-specific questions, fill with category-themed generic or just generic
            final_quiz = quiz_questions + GENERIC_QUESTIONS
            random.shuffle(final_quiz)
            
            for q_data in final_quiz[:8]: # 8 questions per day
                Question.objects.create(
                    day=day,
                    text=q_data["text"],
                    options=q_data["options"],
                    correct_option_index=q_data["correct"]
                )
            
            # Interview Questions
            interview_pool = content.get("interview", [
                {"level": "Junior", "q": f"Basic concept of {topic}?", "a": f"Core fundamentals of {topic}.", "type": "Conceptual"},
                {"level": "Mid", "q": f"How to implement {topic} effectively?", "a": "Standard industry patterns and best practices.", "type": "Scenario"}
            ])
            for i_data in interview_pool:
                InterviewQuestion.objects.create(
                    day=day,
                    question=i_data["q"],
                    answer=i_data["a"],
                    level=i_data["level"],
                    type=i_data["type"]
                )

    print("Success! Database seeded with high-quality, topic-specific content for 10 weeks.")

if __name__ == '__main__':
    seed()
